package BJ;

import java.util.Scanner;

public class BJ_1000 {
    public static void main(String[] args) {
        // 입력을 받기위해 Scanner 변수 선언, 객체 생성 및 할당
        Scanner sc = new Scanner(System.in);
        // A, B값 받기
        int A = sc.nextInt();
        int B = sc.nextInt();
        // A, B 더해서 출력하기
        System.out.print(A + B);
    }
}
